#pragma once

/**
 * @brief Линейная модель объекта управления
 * @param y текущее значение выходной величины
 * @param u управляющее воздействие
 * @param a коэффициент модели
 * @param b коэффициент модели
 * @return новое значение выходной величины
 */
double linear(double y, double u, double a, double b);

/**
 * @brief Структура коэффициентов нелинейной модели объекта управления
 *
 * Содержит параметры, используемые при расчёте выходной величины по нелинейной модели:
 * - @c a — коэффициент линейной части
 * - @c b — коэффициент квадратичной нелинейности
 * - @c c — коэффициент управляющего воздействия
 * - @c d — коэффициент синусоидальной нелинейности
 */
struct NonLinearCoeffs {
    double a; ///< коэффициент линейной части
    double b; ///< коэффициент квадратичной нелинейности
    double c; ///< коэффициент управляющего воздействия
    double d; ///< коэффициент синусоидальной нелинейности
};

/**
 * @brief Нелинейная модель объекта управления
 * @param y текущее значение выходной величины
 * @param y_p предыдущее значения выходной величины
 * @param u управляющее воздействие
 * @param u_p предыдущее управляющее воздействие
 * @param coeffs коэффициенты модели
 * @return новое значение выходной величины
 */
double non_linear(double y, double y_p, double u, double u_p, const NonLinearCoeffs& coeffs);